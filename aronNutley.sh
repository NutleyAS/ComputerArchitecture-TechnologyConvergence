#!/bin/bash

# CATC Project Q2.2

# blank echo commands gives space between echo statements and command executions

# 'set -o history'  enables history for the script

set -o history > aronNutley.txt

echo "CATC Project Q2.2"  >> aronNutley.txt 

echo "     " >> aronNutley.txt

echo "Starting ShellScript..." >> aronNutley.txt

echo "     " >> aronNutley.txt

echo "date" >> aronNutley.txt
date >> aronNutley.txt

echo "     " >> aronNutley.txt

echo "hostname" >> aronNutley.txt
hostname >> aronNutley.txt

echo "     " >> aronNutley.txt

echo "arch" >> aronNutley.txt
arch >> aronNutley.txt

echo "    " >> aronNutley.txt

echo "uname -a" >> aronNutley.txt 
uname -a >> aronNutley.txt

echo "     " >> aronNutley.txt

echo "uptime" >> aronNutley.txt
uptime >> aronNutley.txt

echo "     " >> aronNutley.txt

echo "whoami" >> aronNutley.txt
whoami >> aronNutley.txt

echo "     " >> aronNutley.txt

echo "who" >> aronNutley.txt
who >> aronNutley.txt

echo "    " >> aronNutley.txt

echo "finger" >> aronNutley.txt
finger >> aronNutley.txt

echo "     " >> aronNutley.txt

echo "w" >> aronNutley.txt
w >> aronNutley.txt

echo "     " >> aronNutley.txt

echo "top" >> aronNutley.txt
top >> aronNutley.txt

echo "        " >> aronNutley.txt


echo "history" >> aronNutley.txt

history >> aronNutley.txt

echo "     " >> aronNutley.txt

echo "ShellScript Complete" >> aronNutley.txt

echo "     " >> aronNutley.txt
